// Get current language from localStorage or default to Russian
function getCurrentLanguage() {
    return localStorage.getItem('geekposters-language') || 'ru';
}

// Set current language and save to localStorage
function setLanguage(lang) {
    if (translations[lang]) {
        localStorage.setItem('geekposters-language', lang);
        updatePageTranslations(lang);
        return true;
    }
    return false;
}

// Translate a key
function t(key) {
    const lang = getCurrentLanguage();
    return translations[lang][key] || translations['en'][key] || key;
}

// Update all translations on the page
function updatePageTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = translations[lang][key] || translations['en'][key] || key;
    });
    
    // Update placeholder attributes
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = translations[lang][key] || translations['en'][key] || key;
    });
}

// Initialize translations on page load
document.addEventListener('DOMContentLoaded', function() {
    const lang = getCurrentLanguage();
    updatePageTranslations(lang);
});
